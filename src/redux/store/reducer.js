// --------------------------------------------------------------
// --------------------------------------------------------------
const initialState = {
  user: null
};

// --------------------------------------------------------------
// --------------------------------------------------------------
export default function( state = initialState, action ) {

  switch(action.type) {
    // --------------------------------------------------------------
    case "SIGN_IN":
      if(action.user===null) return {...state, user: null};
      return { ...state, user: action.user };
      break;
    // --------------------------------------------------------------
    default:
      return state;
  }
};